function checkText1() {
    // Text saved in JavaScript
    const savedText = "hydrogen"; // Change this to your desired text

    // Get the value entered by the user
    const enteredText = document.getElementById("textInput").value;

    // Check if the entered text matches the saved text
    if (enteredText === savedText) {
        // If texts match, proceed with the href link
        const link = document.getElementById("myLink").href;
        window.location.href = link; // Redirect to the href link
    } else {
        // If texts don't match, alert the user
        alert("Entered passkey does not match the required passkey.");
    }
}

